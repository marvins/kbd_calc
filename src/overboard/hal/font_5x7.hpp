inline constexpr int UNICODE_GLYPH_COUNT = static_cast<int>(std::size(UNICODE_GLYPHS));

// Decode one UTF-8 codepoint from str at byte offset pos.
// Advances pos past the consumed bytes. Returns 0xFFFD on error.
inline uint32_t utf8_decode(const std::string& str, size_t& pos) {
    if (pos >= str.size()) return 0;
    uint8_t b0 = static_cast<uint8_t>(str[pos]);
    if (b0 < 0x80) { ++pos; return b0; }
    if ((b0 & 0xE0) == 0xC0) {
        if (pos + 1 >= str.size()) { ++pos; return 0xFFFD; }
        uint32_t cp = (static_cast<uint32_t>(b0 & 0x1F) << 6)
                    | static_cast<uint32_t>(static_cast<uint8_t>(str[pos+1]) & 0x3F);
        pos += 2; return cp;
    }
    if ((b0 & 0xF0) == 0xE0) {
        if (pos + 2 >= str.size()) { ++pos; return 0xFFFD; }
        uint32_t cp = (static_cast<uint32_t>(b0 & 0x0F) << 12)
                    | (static_cast<uint32_t>(static_cast<uint8_t>(str[pos+1]) & 0x3F) << 6)
                    |  static_cast<uint32_t>(static_cast<uint8_t>(str[pos+2]) & 0x3F);
        pos += 3; return cp;
    }
    ++pos; return 0xFFFD;
}

// Get glyph rows for a codepoint (nullptr if not found)
inline const std::array<uint8_t, 7>* get_glyph(uint32_t codepoint) {
    // Check basic ASCII range
    if (codepoint >= FONT_FIRST && codepoint <= FONT_LAST) {
        return &FONT_5X7[codepoint - FONT_FIRST];
    }
    // Check extended Unicode glyphs
    for (int i = 0; i < UNICODE_GLYPH_COUNT; ++i) {
        if (UNICODE_GLYPHS[i].codepoint == codepoint) {
            return &UNICODE_GLYPHS[i].rows;
        }
    }
    return nullptr;
}

