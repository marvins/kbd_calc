static const int FONT_FIRST = 32; // ASCII ' '
static const int FONT_LAST  = 32 + static_cast<int>(sizeof(FONT_5X7)/sizeof(FONT_5X7[0])) - 1;

// Extended Unicode glyphs — 5x7, same bit format as FONT_5X7
struct Unicode_Glyph {
    uint32_t codepoint;
    uint8_t  rows[7];
};

